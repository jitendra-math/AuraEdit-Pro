/* public/sw.js */

const CACHE_NAME = 'aura-svelte-v3.0';

// 1. Files to cache immediately (Core Shell)
const STATIC_ASSETS = [
  './',
  './index.html',
  './manifest.json',
  './icons/icon-192.png',
  './icons/icon-512.png',
  // External Fonts/Icons
  'https://cdn.jsdelivr.net/npm/remixicon@4.1.0/fonts/remixicon.css',
  'https://fonts.googleapis.com/css2?family=JetBrains+Mono:wght@400;500&family=Outfit:wght@300;400;500;600&display=swap'
];

// 2. Install Event
self.addEventListener('install', (event) => {
  self.skipWaiting(); // Activate immediately
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => {
      console.log('[SW] Caching App Shell');
      return cache.addAll(STATIC_ASSETS);
    })
  );
});

// 3. Activate Event (Cleanup Old Caches)
self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((keys) => {
      return Promise.all(
        keys.map((key) => {
          if (key !== CACHE_NAME) {
            console.log('[SW] Removing old cache:', key);
            return caches.delete(key);
          }
        })
      );
    })
  );
  self.clients.claim(); // Take control of all clients
});

// 4. Fetch Event (Network First, then Cache)
// Strategy: Try Network -> If Fail, Try Cache -> If Fail, Return Offline Page (index.html)
self.addEventListener('fetch', (event) => {
  // Skip cross-origin requests logic if needed, but we want fonts
  if (event.request.method !== 'GET') return;

  event.respondWith(
    fetch(event.request)
      .then((networkResponse) => {
        // Clone response to put in cache
        const clone = networkResponse.clone();
        
        caches.open(CACHE_NAME).then((cache) => {
           // Cache JS/CSS chunks generated by Vite
           if (event.request.url.includes('assets/') || 
               event.request.destination === 'script' || 
               event.request.destination === 'style') {
              cache.put(event.request, clone);
           }
        });
        
        return networkResponse;
      })
      .catch(() => {
        // Network failed, try cache
        return caches.match(event.request).then((cachedResponse) => {
          if (cachedResponse) return cachedResponse;
          
          // If everything fails and it's a navigation, return index.html
          if (event.request.mode === 'navigate') {
            return caches.match('./index.html');
          }
        });
      })
  );
});
